export const slideIn = (direction: 'left' | 'right' | 'up' | 'down', delay = 0) => {
  return {
    hidden: {
      x: direction === 'left' ? '-100%' : direction === 'right' ? '100%' : 0,
      y: direction === 'up' ? '100%' : direction === 'down' ? '-100%' : 0,
    },
    visible: {
      x: 0,
      y: 0,
      transition: {
        type: 'tween',
        delay,
        duration: 0.8,
        ease: 'easeOut',
      },
    },
  };
};

